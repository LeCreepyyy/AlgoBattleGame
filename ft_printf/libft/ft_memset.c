#include "libft.h"

void	*ft_memset(void *w, int n, size_t len)
{
	char	*e;
	size_t	i;

	e = w;
	i = 0;
	while (i < len)
	{
		e[i] = (char)n;
		i++;
	}
	w = e;
	return (w);
}
